public class Cart {

    private Product[] products;
    private User user;

    public Cart(Product[] products, User user) {
        this.products = products;
        this.user = user;
    }

    public Product[] getProducts() {
        return products;
    }

    public void setProducts(Product[] products) {
        this.products = products;
    }

    public User getUser() {
        return user;
    }

    public void setUser(User user) {
        this.user = user;
    }
}
